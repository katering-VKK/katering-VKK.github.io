export function parsePrice(price: string): number {
  return parseInt(price.replace(/\s/g, '').replace('₴', ''), 10) || 0;
}

const categoryHueRanges: Record<string, [number, number]> = {
  'Книги': [0, 40],
  'Іграшки': [190, 250],
  'Власне виробництво': [30, 55],
  'Творчість': [270, 330],
  'Настільні ігри': [90, 160],
};

export function getProductGradient(id: number, category: string): string {
  const [hueMin, hueMax] = categoryHueRanges[category] ?? [0, 360];
  const range = hueMax - hueMin;
  const hue1 = hueMin + ((id * 37) % range);
  const hue2 = hueMin + (((id * 37) + range * 0.4) % range);
  const sat1 = 55 + (id % 30);
  const sat2 = 50 + ((id * 3) % 35);
  const light1 = 60 + (id % 20);
  const light2 = 65 + ((id * 7) % 18);
  return `linear-gradient(135deg, hsl(${hue1}, ${sat1}%, ${light1}%) 0%, hsl(${hue2}, ${sat2}%, ${light2}%) 100%)`;
}

